/*
Problem: Given two integers A and B. Your task is to return their product.

Link: https://www.geeksforgeeks.org/problems/c-input-output2432/1

Approach:
- Just use the multiplication operator (*).

Time Complexity: O(1)
Space Complexity: O(1)
*/

class Solution {
    static int multiplication(int A, int B) {
        
        return A*B;
    }
}
